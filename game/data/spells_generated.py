def get_spells_for_tier(tavern_tier):
    """Return all Tavern spells available up to and including this tavern tier."""
    return [spell for spell in SPELLS.values() if spell["tier"] <= tavern_tier]


def get_spells_by_tier(tier):
    """Return all Tavern spells exactly matching the given tier."""
    return SPELLS_BY_TIER.get(tier, [])


def get_spell(spell_id):
    """Return one spell by id, or None if it does not exist."""
    return SPELLS.get(spell_id)
