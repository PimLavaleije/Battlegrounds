def get_trinket(trinket_id):
    """Return one trinket by id."""
    return TRINKETS[trinket_id]


def find_trinkets_by_name(name):
    """Return all trinkets whose name matches exactly, case-insensitive."""
    n = name.lower()
    return [t for t in TRINKETS.values() if t["name"].lower() == n]


def get_trinkets_for_tribe(tribe):
    """Return trinkets whose inferred description mentions a tribe."""
    return [t for t in TRINKETS.values() if tribe in t.get("tribes", [])]


def get_trinkets_with_ability(ability):
    """Return trinkets whose inferred ability list contains `ability`."""
    return [t for t in TRINKETS.values() if ability in t.get("abilities", [])]
